  '7' => '--...',
  '8' => '---..',
  '9' => '----.',
  '0' => '-----'}

  def initialize
  end

  def eng_to_morse(words)
    translation = words.downcase.split('')
    translation.map {|key| Morsecode[key]}.join("")
  end
end
